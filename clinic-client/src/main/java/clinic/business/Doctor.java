package clinic.business;



public class Doctor {
	private int id;
	
	private String ten;
	
	
	private int cmt;
	
	private String ngaysinh;
	
	
	private String diachi;
	
	
	private String bacnghe;
	
	
	private String thamnien;
	
	
	private String trinhdo;
	
	
	private String chuyenmon;


	public int getId() {
		return id;
	}


	public void setId(int id) {
		this.id = id;
	}


	public String getTen() {
		return ten;
	}


	public void setTen(String ten) {
		this.ten = ten;
	}


	public int getCmt() {
		return cmt;
	}


	public void setCmt(int cmt) {
		this.cmt = cmt;
	}


	public String getNgaysinh() {
		return ngaysinh;
	}


	public void setNgaysinh(String ngaysinh) {
		this.ngaysinh = ngaysinh;
	}


	public String getDiachi() {
		return diachi;
	}


	public void setDiachi(String diachi) {
		this.diachi = diachi;
	}


	public String getBacnghe() {
		return bacnghe;
	}


	public void setBacnghe(String bacnghe) {
		this.bacnghe = bacnghe;
	}


	public String getThamnien() {
		return thamnien;
	}


	public void setThamnien(String thamnien) {
		this.thamnien = thamnien;
	}


	public String getTrinhdo() {
		return trinhdo;
	}


	public void setTrinhdo(String trinhdo) {
		this.trinhdo = trinhdo;
	}


	public String getChuyenmon() {
		return chuyenmon;
	}


	public void setChuyenmon(String chuyenmon) {
		this.chuyenmon = chuyenmon;
	}
	
	
}
